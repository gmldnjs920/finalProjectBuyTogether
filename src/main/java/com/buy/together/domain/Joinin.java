package com.buy.together.domain;

public class Joinin {

	private int joinin_number;
	private int user_numner;
	private int matching_status_number;
	private int buyTogether_number;

	public Joinin() {

	}

	public int getJoinin_number() {
		return joinin_number;
	}

	public void setJoinin_number(int joinin_number) {
		this.joinin_number = joinin_number;
	}

	public int getUser_numner() {
		return user_numner;
	}

	public void setUser_numner(int user_numner) {
		this.user_numner = user_numner;
	}

	public int getMatching_status_number() {
		return matching_status_number;
	}

	public void setMatching_status_number(int matching_status_number) {
		this.matching_status_number = matching_status_number;
	}

	public int getBuyTogether_number() {
		return buyTogether_number;
	}

	public void setBuyTogether_number(int buyTogether_number) {
		this.buyTogether_number = buyTogether_number;
	}
	

}
